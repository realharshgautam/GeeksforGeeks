/*
structure of the node of the binary tree is as
struct Node
{
    int data;
    struct Node *left;
    struct Node *right;

    Node(int x)
    {
        data = x;
        left = NULL;
        right = NULL;
    }
};
*/
class Solution
{
public:
    void solve(Node* root, int level, int sum, vector<pair<int, int>> &v){
         if(!root->left && !root->right){
             v.push_back({level, sum});
             return;
         }
         if(root->left){
             solve(root->left, level+1, sum+root->left->data, v);
         }
         if(root->right){
             solve(root->right, level+1, sum+root->right->data, v);
         }
    }
    
    int sumOfLongRootToLeafPath(Node *root){
        vector<pair<int, int>> v;
        solve(root, 0, root->data, v);
        int ans = 0;
        int ind = 0;
        for(auto it: v){
            if(it.first > ind){
                ind = it.first;
                ans = it.second;
            }
            else if(it.first == ind){
                ans = max(it.second, ans);
            }
        }
        return ans;
    }
};

